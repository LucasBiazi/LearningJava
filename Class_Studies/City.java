public class City {
    private int code;
    private String name;

    // Getters
    public int get_code() {
        return code;
    }

    public String get_name() {
        return name;
    }

    // Setters
    public void setCode(int code) {
        this.code = code;
    }

    public void setName(String name) {
        this.name = name;
    }
}
